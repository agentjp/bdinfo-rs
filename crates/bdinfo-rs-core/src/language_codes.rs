//! ISO-639 language-code -> English-name lookup.
//!
//! Each code maps to a display name; an unrecognized code maps to itself, so a
//! stream's language field is never lost — at worst the raw code is shown.
//!
//! The mapping is a `static` table plus a linear scan (the
//! same shape as [`crate::discovery`]'s small lookup tables) — deliberately not a
//! hash map: the result feeds the stream model's language name, an output-path
//! value whose ordering must stay deterministic (the house rule: never a hash
//! map in an output path). The table is stored sorted by code; the lookup is an
//! exact match on the code, so storage order is non-observable.

