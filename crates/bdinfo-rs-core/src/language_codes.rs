/// Returns the English name for an ISO-639 language `code`, or `code` itself when
/// it is not in the table.
///
/// The match is exact and case-sensitive; anything unrecognized falls back to
/// the identity mapping, so the caller always gets a printable value.
#[must_use]
pub fn get_name(code: &str) -> String {
    LANGUAGE_CODES
        .iter()
        .find(|&&(candidate, _)| candidate == code)
        .map_or_else(|| code.to_owned(), |&(_, name)| name.to_owned())
}

#[cfg(test)]
mod tests {
    use proptest::prelude::{prop_assert_eq, proptest};

    use super::{LANGUAGE_CODES, get_name};

    #[test]
    fn known_codes_map_to_their_names() {
        assert_eq!(get_name("eng"), "English");
        assert_eq!(get_name("jpn"), "Japanese");
        assert_eq!(get_name("fra"), "French");
        assert_eq!(get_name("aar"), "Afar"); // first by code
        assert_eq!(get_name("zun"), "Zuni"); // last by code
    }

    #[test]
    fn iso_639_b_and_t_aliases_share_a_name() {
        // Both ISO-639-2 variants resolve to the same English name.
        assert_eq!(get_name("ger"), "German");
        assert_eq!(get_name("deu"), "German");
        assert_eq!(get_name("alb"), "Albanian");
        assert_eq!(get_name("sqi"), "Albanian");
    }

    #[test]
    fn non_ascii_names_are_preserved_verbatim() {
        // The report emits these exact UTF-8 bytes; keep them byte-for-byte.
        assert_eq!(get_name("gwi"), "Gwich\u{00B4}in");
        assert_eq!(get_name("nob"), "Norwegian Bokm\u{00E5}l");
        assert_eq!(get_name("pro"), "Proven\u{00E7}al");
        assert_eq!(get_name("vol"), "Volap\u{00FC}k");
    }

    #[test]
    fn unknown_code_returns_itself() {
        // The identity fallback: an unrecognized code maps to itself.
        assert_eq!(get_name("xyz"), "xyz");
        assert_eq!(get_name(""), "");
    }

    #[test]
    fn lookup_is_case_sensitive() {
        // The table matches the lowercase code exactly; uppercase falls
        // through to the identity fallback.
        assert_eq!(get_name("ENG"), "ENG");
        assert_eq!(get_name("Eng"), "Eng");
    }

    #[test]
    fn table_is_sorted_by_code_without_duplicates() {
        // Strictly ascending => sorted (the doc claim) and no duplicate codes.
        assert!(LANGUAGE_CODES.is_sorted_by(|a, b| a.0 < b.0));
    }

    #[test]
    fn every_table_entry_round_trips() {
        // Every code resolves to its own name, exercising the scan at all
        // positions (first, last, and everything between).
        for &(code, name) in LANGUAGE_CODES {
            assert_eq!(get_name(code), name);
        }
    }

    proptest! {
        #[test]
        fn uppercase_codes_are_never_found(code in "[A-Z]{3}") {
            // The table holds only lowercase codes, so any uppercase triple hits
            // the identity fallback.
            prop_assert_eq!(get_name(&code), code);
        }
    }
}
